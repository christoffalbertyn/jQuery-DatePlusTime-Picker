/* Inicialización en español para la extensión 'dateplustimepicker' para jQuery. */
jQuery(function($){
	$.dateplustimepicker.regional['es'] = {
        hourText: 'Horas',
        minuteText: 'Minutos',
        secondText: 'Segundos',
        timeFormat: 'hh:mm tt',
        timeOnlyTitle: 'Seleccione una hora',
        timeText: 'Hora'
	};
	$.dateplustimepicker.setDefaults($.dateplustimepicker.regional['es']);
});